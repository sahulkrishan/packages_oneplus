package com.oneplus.settings.opfinger;

public class OPFingerPrintEntity {
    private int opfingerId;
    private String opfingerName;

    public int getOpfingerId() {
        return this.opfingerId;
    }

    public void setOpfingerId(int opfingerId) {
        this.opfingerId = opfingerId;
    }

    public String getOpfingerName() {
        return this.opfingerName;
    }

    public void setOpfingerName(String opfingerName) {
        this.opfingerName = opfingerName;
    }
}
